import dotenv from 'dotenv'
import path from 'path'

// Load environment variables
dotenv.config({ path: path.join(__dirname, '../.env') })

export interface ActivityBatchItem {
  capturedAt: string
  mouseDelta: number
  keyCount: number
  activeSeconds?: number
  deviceSessionId?: string
}

const API_URL = process.env.API_URL || 'https://dexterzbackend.online/api'

console.log('\n========== API CLIENT CONFIG ==========');
console.log('API_URL:', API_URL);
console.log('process.env.API_URL:', process.env.API_URL);
console.log('========================================\n');

export class ApiClient {
  private token: string | null = null
  
  get tokenValue(): string | null {
    return this.token
  }

  setToken(token: string) {
    this.token = token
  }

  clearToken() {
    this.token = null
  }

  private async request<T>(endpoint: string, options: RequestInit = {}): Promise<T> {
    const headers: Record<string, string> = {
      'Content-Type': 'application/json',
      ...(options.headers as Record<string, string>),
    };

    if (this.token) {
      headers['Authorization'] = `Bearer ${this.token}`
    }

    const response = await fetch(`${API_URL}${endpoint}`, {
      ...options,
      headers,
    })

    if (!response.ok) {
      const error: any = await response.json().catch(() => ({ message: 'Request failed' }))
      throw new Error(error.message || 'Request failed')
    }

    if (response.status === 204) {
      return {} as T
    }

    return response.json() as Promise<T>
  }

  async login(email: string, password: string) {
    const data = await this.request<{ accessToken: string; refreshToken: string }>(
      '/auth/login',
      {
        method: 'POST',
        body: JSON.stringify({ email, password }),
      }
    )
    this.setToken(data.accessToken)
    return data
  }

  async logout() {
    try {
      await this.request('/auth/logout', { method: 'POST' })
    } catch (error) {
      console.error('Logout API call failed:', error)
      // Continue with token clearing even if API call fails
    } finally {
      this.token = null
    }
  }

  async startSession(data: { deviceId: string; platform: string }) {
    return this.request<{ id: string }>('/activity/sessions/start', {
      method: 'POST',
      body: JSON.stringify(data),
    })
  }

  async stopSession(sessionId: string) {
    return this.request('/activity/sessions/stop', {
      method: 'POST',
      body: JSON.stringify({ sessionId }),
    })
  }

  async uploadActivity(samples: ActivityBatchItem[]) {
    return this.request('/activity/batch', {
      method: 'POST',
      body: JSON.stringify({ samples }),
    })
  }

  async getOrganization() {
    return this.request<{ id: string; name: string; timezone: string }>('/organizations/me')
  }

  async getSchedule() {
    return this.request<{
      tz: string
      checkinStart: string
      checkinEnd: string
      breakStart: string
      breakEnd: string
      idleThresholdSeconds: number
    }>('/organizations/schedule')
  }

  async getMe() {
    return this.request<{
      id: string
      email: string
      fullName: string
      role: string
      customCheckinStart: string | null
      customCheckinEnd: string | null
    }>('/auth/me')
  }

  async triggerRollup() {
    return this.request('/activity/rollup', {
      method: 'POST',
    })
  }
}
